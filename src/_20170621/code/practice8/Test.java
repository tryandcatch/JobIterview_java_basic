public class Test {
    static int i;
    public static void main(String[] args){
        System.out.println(i);
    }
    /**
     * 解释:
     * static修饰的变量（即静态变量）会在编译期间由系统自动初始化并赋予默认值
     * int>0
     * byte>0
     * float>0.0
     * short>0
     * double>0.0
     * char>  (空字符) ASCLL 值为0
     * long>0
     * boolean>false
     */
}
